package com.motivity.Statements.in;

public class IfStatementExample2 {

    public static void main(String args[])
    {
        String str = "GoodMorning";
        int i = 4;

        // if block
        if (i == 4) {
            i++;
            System.out.println(str);
        }

        // Executed by default
        System.out.println("i = " + i);
    }
}
