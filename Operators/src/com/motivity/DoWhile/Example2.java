package com.motivity.DoWhile;

public class Example2 {

    public static void main(String args[])
    {

        // Declaring and initialization expression
        int i = 1;

        // Do-while loop
        do {

            // Body od do-while loop
            // Print statement
            System.out.println("Hello World");

            // Update expression
            i++;
        }

        // Test expression
        while (i < 6);
    }
}
